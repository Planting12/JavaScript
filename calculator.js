const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },

  min: function (a, b) {
    console.log(a - b);
  },

  div: function (a, b) {
    console.log(a / b);
  },

  powerof: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(20, 23);
calculator.min(20, 23);
calculator.div(20, 23);
calculator.powerof(20, 23);
